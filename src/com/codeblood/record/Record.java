package com.codeblood.record;

/**
 * 
 * @Author: Star
 * 
 * Description: 对记录信息进行封装和管理
 * 
 */
public class Record
{
	protected int win;
	protected int count;

	//get and set
	public int getWin()
	{
		return win;
	}
	public void setWin(int win)
	{
		this.win = win;
	}
	public int getCount()
	{
		return count;
	}
	public void setCount(int count)
	{
		this.count = count;
	}
}
